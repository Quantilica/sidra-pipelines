SELECT
	'censo' AS FONTE,
	ANO,
	ID_MUNICIPIO,
	NOME_MUNICIPIO,
	N_PESSOAS
FROM
	ANALYTICS.CENSO_POPULACAO
UNION ALL
SELECT
	'contagem' AS FONTE,
	ANO,
	ID_MUNICIPIO,
	NOME_MUNICIPIO,
	N_PESSOAS
FROM
	ANALYTICS.CONTAGEM_POPULACAO
UNION ALL
SELECT
	'estimativa' AS FONTE,
	ANO,
	ID_MUNICIPIO,
	NOME_MUNICIPIO,
	N_PESSOAS
FROM
	ANALYTICS.ESTIMATIVA_POPULACAO;
